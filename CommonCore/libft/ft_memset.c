#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	unsigned char	*s2;

	i = 0;
	s2 = (unsigned char *)s;
	while (n--)
		s2[i++] = c;
	return (s);
}
