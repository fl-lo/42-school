#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*s3;
	unsigned char	*s4;

	i = 0;
	s3 = (unsigned char *)s1;
	s4 = (unsigned char *)s2;
	while (s3[i] && s4[i] && i < n)
	{
		if (s3[i] != s4[i])
			return ((unsigned char)s3[i] - s4[i]);
		i++;
	}
	if (n == i)
		return (0);
	return ((unsigned char)s3[i] - s4[i]);
}
