char	*clean_str(char *str)
{
	int	i;

	i = 0;
	while (str[i] == '0')
		i++;
	if (str[i] >= '1' && str[i] <= '9')
		str = &str[i];
	else if (str[i] == '\0')
	{
		str = &str[i - 1];
	}
	return (str);
}
